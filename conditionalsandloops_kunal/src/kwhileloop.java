public class kwhileloop {
    public static void main(String[] args) {
        //while loop
        /*
        while(condition)
        { // body
        }
         */
        // 1 to 5
        int num=1;
        while(num<=5){
            System.out.println(num);
            num++;
        }
        //do while loop loop
        // loop execute atleast once then it check
        int ew=1;
        do{
            System.out.println("dffdgdgd");
        }
        while(ew!=1);
    }
}
